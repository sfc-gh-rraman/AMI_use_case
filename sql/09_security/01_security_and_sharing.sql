USE DATABASE AMI_DEMO;
USE SCHEMA AMI_CURATED;

-- ---- Role -> territory map -------------------------------------------
CREATE OR REPLACE TABLE ROLE_TERRITORY_MAP (
  ROLE_NAME         VARCHAR,
  UTILITY_TERRITORY VARCHAR
);

